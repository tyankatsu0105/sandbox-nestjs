export type Maybe<T> = T | null;
export type Exact<T extends { [key: string]: unknown }> = { [K in keyof T]: T[K] };
/** All built-in and custom scalars, mapped to their actual values */
export type Scalars = {
  ID: string;
  String: string;
  Boolean: boolean;
  Int: number;
  Float: number;
};

export type Query = {
  cats?: Maybe<Array<Maybe<Cat>>>;
};

export type Cat = {
  name: Scalars['String'];
  age?: Maybe<Scalars['Int']>;
};
